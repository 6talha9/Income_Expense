package model;

public class Income {
int incmid, catid, uid, amount;
String date;


public int getIncmid() {
	return incmid;
}
public void setIncmid(int incmid) {
	this.incmid = incmid;
}
public int getCatid() {
	return catid;
}
public void setCatid(int catid) {
	this.catid = catid;
}
public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public int getAmount() {
	return amount;
}
public void setAmount(int amount) {
	this.amount = amount;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}

}
